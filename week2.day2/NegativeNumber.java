public class NegativeNumber {
	public static void main(String[] args) {
		int i = 35;
		
		if (i>0)
			System.out.println(i + " is Positive number");
		else if (i < 0)
			System.out.println(i + " is Negative number");
		else 
			System.out.println(i + " is neither Positive nor Negative");
	}

}
